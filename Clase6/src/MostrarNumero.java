public class MostrarNumero {
    public static void main(String[] args) {
        // Muestra los números del 1 al 100 utilizando un bucle while.
        int contador = 0;
        //Mientras que la condición sea verdadera se seguira en el bucle while
        while(contador < 100){
            contador++;
            System.out.println(contador);
        }
    }
}
